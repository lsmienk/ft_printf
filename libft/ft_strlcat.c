#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t src_size;
	size_t dst_size;

	src_size = ft_strlen((char *)src);
	dst_size = ft_strlen((char *)dst);
	i = dst_size;
	if (size < dst_size)
		return (src_size + size);
	while (*src && i < (size - 1))
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return (dst_size + src_size);
}
